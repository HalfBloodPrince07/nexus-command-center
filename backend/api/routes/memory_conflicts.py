class ConflictStatus(str, Enum):
    unresolved = "unresolved"
    reviewing = "reviewing"
    resolved_keep_a = "resolved_keep_a"
    resolved_keep_b = "resolved_keep_b"
    resolved_merged = "resolved_merged"
    dismissed = "dismissed"


class ConflictResolutionAction(str, Enum):
    keep_a = "keep_a"
    keep_b = "keep_b"
    merge = "merge"
    both = "both"
    dismiss = "dismiss"


class ConflictCreateRequest(BaseModel):
    """Request to create a conflict (usually auto-generated by system)."""

    fact_a_id: str = Field(..., min_length=1)
    fact_b_id: str = Field(..., min_length=1)
    conflict_type: str = Field("factual", pattern=r"^(factual|temporal|preferential)$")
    explanation: str | None = Field(None, max_length=500)


class ConflictResponse(BaseModel):
    """Full conflict record with both facts loaded."""

    id: str
    fact_a: dict[str, Any]
    fact_b: dict[str, Any]
    conflict_type: str
    explanation: str | None
    status: ConflictStatus
    resolution_notes: str | None
    detected_at: str
    resolved_at: str | None


class ListConflictsResponse(BaseModel):
    conflicts: list[ConflictResponse]
    total: int


class ResolveConflictRequest(BaseModel):
    action: ConflictResolutionAction
    resolution_notes: str | None = Field(None, max_length=1000)


class ProposeMergeResponse(BaseModel):
    merged_fact: str
    confidence: float
    proposed_category: str
    explanation: str


