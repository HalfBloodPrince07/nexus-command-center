class ConflictNotFound(Exception):
    def to_response(self) -> tuple[dict[str, Any], int]:
        return {"error": "Conflict not found"}, HTTPStatus.NOT_FOUND


class ConflictNotFoundOrAlreadyResolved(Exception):
    def to_response(self) -> tuple[dict[str, Any], int]:
        return {"error": "Conflict not found or already resolved"}, HTTPStatus.NOT_FOUND


